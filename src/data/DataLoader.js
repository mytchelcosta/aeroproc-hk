// ============================================================
// DataLoader.js - The ICAO Data Fetcher and Parser
// ============================================================
// This module is responsible for fetching the raw ICAO data files
// from the /data/ folder on the server and parsing them into
// clean JavaScript objects that the rest of the app can use.
//
// The files follow a specific format:
//   - Lines starting with ';' are comments and must be skipped.
//   - Data columns are separated by TAB characters (\t).
//
// Every fetch function is 'async', meaning it returns a Promise.
// This means the browser can fetch the file in the background
// without freezing the entire app while it waits. The app simply
// 'awaits' the result when it needs the data.
//
// The /data/ URL path is made accessible by a custom plugin
// configured inside vite.config.js.
// ============================================================

