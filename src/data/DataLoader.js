// Loads runway threshold coordinates from the Architect-supplied runways.json file.
// The JSON contains precise decimal-degree coordinates for SBGR, SBSP, and SBKP.
//
// runways.json format: [{ "id": "SBGR 10L", "name": "...", "lat": ..., "lng": ... }, ...]
// We normalize this to the internal shape used throughout the app:
//   { ident, name, lat, lon, airport, tipo }
// where 'airport' is extracted from the id (e.g. 'SBGR' from 'SBGR 10L')
// and 'tipo' is always 'RWY' so MapLayers can style thresholds distinctly.
//
// Returns: synchronous array — no fetch needed, Vite bundles the JSON at build time.
const loadRunwayThresholds = () => {
  const thresholds = runwaysData.map((entry) => ({
    ident:   entry.id,                              // e.g. 'SBGR 10L'
    name:    entry.name,                            // e.g. 'Guarulhos 10L (ex 09L)'
    lat:     entry.lat,                             // precise decimal latitude
    lon:     entry.lng,                             // normalize 'lng' → 'lon' for consistency
    airport: entry.id.split(' ')[0] || 'UNKN',     // extract 'SBGR' from 'SBGR 10L'
    tipo:    'RWY'
  }));

  console.log(`[DataLoader] Loaded ${thresholds.length} runway thresholds from runways.json.`);
  return thresholds;
};

// The major airports that receive the large, bright airplane icon and are ON by default.
const TIER_1_ICAO = new Set([
  'VHHH', 'VMMC', 'ZGSZ', 'ZGGG'
]);

// Fixed-wing airport types from the OurAirports schema.
// Anything in this set (that is NOT in TIER_1_ICAO) becomes Tier 2 (Regional).
const FIXED_WING_TYPES = new Set(['small_airport', 'medium_airport', 'large_airport']);

// Fetches and parses the OurAirports airports.csv file, then filters it to
// the airports within 300 Nautical Miles of SBGR (Guarulhos International).
// This replaces the old hardcoded list of 16 airports with a live, comprehensive
// dataset that automatically includes every airfield in the Hong Kong area.
//
// The CSV is ~12 MB and contains ~85,000 airports globally. PapaParse handles
// this without freezing the browser by parsing the text asynchronously.
//
// Results are split into three tiers so MapLayers can render each tier
// independently (different icon, different default visibility):
//   Tier 1 — Major:    the 16 well-known SP area airports (default ON)
//   Tier 2 — Regional: other fixed-wing airports within 300 NM (default OFF)
//   Tier 3 — Heliport: heliports within 300 NM (default OFF)
//
// Filtering rules (applied in order):
//   1. Exclude airports with type "closed" — they no longer exist operationally.
//   2. The lat/lon fields must be valid numbers (some rows have empty coords).
//   3. The airport must be within 300 NM of SBGR to stay within the SP TMA scope.
//
// Returned shape: { major: [...], regional: [...], heliports: [...] }
//   Each array holds objects of shape { icao, name, lat, lon }.
//   'icao' is the OurAirports 'ident' field (= ICAO code for most airports).
const loadAerodromes = async () => {
  const VHHH_LAT = 22.3089;
  const VHHH_LON = 113.9146;

  try {
    const baseUrl = import.meta.env.BASE_URL || './';
    const response = await fetch(`${baseUrl}data/airports.csv`);
    if (!response.ok) {
      throw new Error(
        `Server returned status ${response.status} (${response.statusText}) for airports.csv.`
      );
    }

    const text = await response.text();
    const result = Papa.parse(text, {
      header:         true,
      skipEmptyLines: true,
      dynamicTyping:  true
    });

    if (result.errors && result.errors.length > 0) {
      console.warn(`[DataLoader] airports.csv parse errors: ${result.errors.length}`);
    }

    const major     = [];
    const regional  = [];
    const heliports = [];

    result.data
      .filter((row) => {
        if (row.type === 'closed') return false;
        const lat = row.latitude_deg;
        const lon = row.longitude_deg;
        if (typeof lat !== 'number' || typeof lon !== 'number') return false;
        return calculateDistance(VHHH_LAT, VHHH_LON, lat, lon) <= 300;
      })
      .forEach((row) => {
        const entry = {
          icao: row.ident,
          name: row.name,
          lat:  row.latitude_deg,
          lon:  row.longitude_deg
        };

        if (TIER_1_ICAO.has(row.ident)) {
          major.push(entry);
        } else if (row.type === 'heliport') {
          heliports.push(entry);
        } else if (FIXED_WING_TYPES.has(row.type)) {
          regional.push(entry);
        }
      });

    console.log(`[DataLoader] airports.csv: ${major.length} major, ${regional.length} regional, ${heliports.length} heliports.`);
    return { major, regional, heliports };

  } catch (err) {
    console.error('[DataLoader] Failed to load airports.csv:', err?.message || err);
    return { major: [], regional: [], heliports: [] };
  }
};


// Fetches and parses the official AIP Brasil NAVAID JSON file.
// This replaces the old navaids.csv (OurAirports) which contained inaccurate,
// outdated entries for Brazilian NAVAIDs. The new source is authoritative data
// extracted directly from AIP Brasil ENR 4.1 (DECEA official publication).
//
// Source file: data/navaids_aip.json — generated by convert_navaids.js from
// a Tabula-extracted CSV of the AIP Brasil ENR 4.1 PDF.
// All entries are already pre-filtered to 300 NM from SBGR.
//
// JSON schema: [{ ident, name, type, frequency_mhz, lat, lon, elevation_ft, mag_declination, hours }]
//
// Returned shape: [{ ident, name, type, freq, lat, lon }, ...]
//   'type' values: 'VOR', 'VOR/DME', 'DVOR/DME', 'DME', 'NDB'
//                 (GP and LOC are ILS components and are filtered out)
//   'freq' = frequency_mhz:
//             VOR/DME types → 108–118 MHz (e.g. 116.9)
//             NDB types     → 0.190–1.750 MHz (e.g. 0.38 = 380 kHz — multiply × 1000 to display)
const loadNavaids = async () => {
  const DISPLAY_TYPES = new Set(['VOR', 'VOR/DME', 'DVOR/DME', 'DME', 'NDB']);

  try {
    const baseUrl = import.meta.env.BASE_URL || './';
    const response = await fetch(`${baseUrl}data/navaids_aip.json`);
    if (!response.ok) {
      throw new Error(
        `Server returned status ${response.status} (${response.statusText}) for navaids_aip.json.`
      );
    }

    const data = await response.json();
    const navaids = data
      .filter((entry) => DISPLAY_TYPES.has(entry.type) && entry.lat != null && entry.lon != null)
      .map((entry) => ({
        ident: entry.ident,
        name:  entry.name,
        type:  entry.type,
        freq:  entry.frequency_mhz,
        lat:   entry.lat,
        lon:   entry.lon
      }));

    console.log(`[DataLoader] navaids_aip.json: ${navaids.length} NAVAIDs.`);
    return navaids;
  } catch (err) {
    console.error('[DataLoader] Failed to load navaids_aip.json:', err?.message || err);
    return [];
  }
};


