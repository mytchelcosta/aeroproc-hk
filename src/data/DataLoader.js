// This function fetches and parses the ICAO_Airports.txt file.
// It returns a Promise that resolves to an array of airport objects.
//
// Returned shape: [{ icao: 'SBGR', name: 'Guarulhos Intl', country: 'Brazil' }, ...]
const loadAirports = async () => {
  try {
    const baseUrl = import.meta.env.BASE_URL || './';
    const response = await fetch(`${baseUrl}data/ICAO_Airports.txt`);

    if (!response.ok) {
      throw new Error(
        `Server returned status ${response.status} (${response.statusText}) for ICAO_Airports.txt.`
      );
    }

    const rawText = await response.text();
    return parseAirportData(rawText);
  } catch (err) {
    console.error('[DataLoader] Failed to load ICAO_Airports.txt:', err?.message || err);
    return [];
  }
};

// This helper function does the actual text parsing for airport data.
// Keeping parsing separate from fetching ensures each function
// does only ONE job (fetching vs. transforming data).
//
// 'rawText' is the multi-line string content of ICAO_Airports.txt.
// Format per line: ICAO_CODE \t AIRPORT_NAME \t COUNTRY
const parseAirportData = (rawText) => {
  const lines = rawText.split('\n');
  const airports = [];

  lines.forEach((line, index) => {
    // Skip empty lines and comment lines (lines starting with ';').
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith(';')) return;

    // Split each line on the TAB character to get individual columns.
    const columns = trimmed.split('\t');

    // A valid airport line must have at least 3 columns.
    if (columns.length < 3) {
      console.warn(`[DataLoader] Airport line ${index + 1} has fewer columns than expected. Skipping: "${trimmed}"`);
      return;
    }

    airports.push({
      icao: columns[0].trim(),
      name: columns[1].trim(),
      country: columns[2].trim()
    });
  });

  console.log(`[DataLoader] Parsed ${airports.length} airports from ICAO_Airports.txt.`);
  return airports;
};

